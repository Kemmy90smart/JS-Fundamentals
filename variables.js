// Exercise: Working with Variables and Data Types

// 1. Declare and initialize variables
let name = "Taiwo"; // string
let age = 37; // number
let isStudent = true; // boolean
let address = { city: "Abeokuta", country: "Nigeria" }; // object
let hobbies = ["reading", "coding", "dancing"]; // array
let undefinedVariable; // undefined
let nullVariable = null; // null

// 2. Print each variable and its type
console.log("Name:", name, "| Type:", typeof name);
console.log("Age:", age, "| Type:", typeof age);
console.log("Is Student:", isStudent, "| Type:", typeof isStudent);
console.log("Address:", address, "| Type:", typeof address);
console.log("Hobbies:", hobbies, "| Type:", typeof hobbies);
console.log("Undefined Variable:", undefinedVariable, "| Type:", typeof undefinedVariable);
console.log("Null Variable:", nullVariable, "| Type:", typeof nullVariable);
